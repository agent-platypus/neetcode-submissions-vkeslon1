class Solution {
public:
    uint32_t reverseBits(uint32_t n) {
        uint32_t ret = (n >> 16) | (n << 16); //2 byte swap
        // 0x44332211-> 0x22114433

        // 0x 0100 0100 0011 0011 0010 0010 0001 0001 -> 0x 0010 0010 0001 0001 0100 0100 0011 0011
        ret = ((ret & 0xFF00FF00) >> 8) | ((ret & 0x00FF00FF) << 8); // 1 byte swap

        // 0x22114433 -> 0x11223344

        // 0x 0010 0010 0001 0001 0100 0100 0011 0011 -> 0x 0001 0001 0010 0010 0011 0011 0100 0100 

        ret = ((ret & 0xF0F0F0F0) >> 4 ) | ((ret & 0x0F0F0F0F) << 4); // 4 bit swap

        
        ret = ((ret & 0xCCCCCCCC) >> 2) | ((ret & 0x33333333) << 2); // 2 bit swap

        // 0x1001 = 9 0x1010 = a 0x1011 = b 0x1100 = c 0x
        // 0x0001 -> 0x0100

        //0x 0001 0001 0010 0010 0011 0011 0100 0100 -> 0x 0100 0100 1000 1000 1100 1100 0001 0001 
        ret = ((ret & 0xAAAAAAAA) >> 1) | ((ret & 0x55555555) << 1); // 1 bit swap

        return ret; 
        // eventually I want 0x 1000 1000 0100 0100 1100 1100 0010 0010 
    }
};
